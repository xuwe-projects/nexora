//! 桌面应用共享 action 声明。
//!
//! 该 crate 集中定义跨应用复用的 GPUI action 类型，让菜单、快捷键和视图处理器引用同一套命令身份。

/// 账户与用户菜单相关的 action。
///
/// 该模块集中声明账户资料、账户设置和退出登录等命令，供侧边栏、菜单和快捷键系统共同引用。
pub mod account;

/// 应用设置窗口相关的 action。
///
/// 该模块声明打开设置窗口的统一命令，并负责注册符合当前平台习惯的快捷键。
pub mod settings;

/// 桌面窗口与应用生命周期相关的 action。
///
/// 该模块集中声明退出应用、最小化窗口、缩放窗口和切换全屏等平台级命令，
/// 并负责注册对应的系统菜单、快捷键和活动窗口处理器。
pub mod window;

/// 初始化共享 action crate。
///
/// 当前函数没有运行时副作用，保留它是为了让应用入口可以显式引用该 crate，
/// 避免未来仅靠静态注册的 action 在链接优化中被意外丢弃。
pub fn init() {}
